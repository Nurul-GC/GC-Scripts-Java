package artesgc.scripts.kotlin

fun main() {
    println("1>2 -> ${1 > 2}")
    println("1<2 -> ${1 < 2}")
    println("1==2 -> ${1 == 2}")
    println("1!=2 -> ${1 != 2}")

    // *************************************
    println("\n1>2 && 3<4 -> ${1 > 2 && 3 < 4}")
    println("1>2 || 3<4 -> ${1 > 2 || 3 < 4}")
}

